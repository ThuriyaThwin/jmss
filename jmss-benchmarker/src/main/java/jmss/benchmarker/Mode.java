package jmss.benchmarker;

/**
 * Running mode of the jmss.benchmarker
 */
public enum Mode
{
    /**
     * No debug output,
     * Report normal times
     */
    DEFAULT,

    /**
     * Debug output,
     * Report normal times
     */
    DEBUG,

    /**
     * No debug output
     * Report times multiplied by 5
     */
    ALBERT

}
